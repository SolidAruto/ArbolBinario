    public void cargarArbol(){
        arbolito = new arbol (jTxt1.getText());
        //Nivel 1
        arbolito.insertarIzquierda(jtxt2.getText(), arbolito.getRaiz());
        arbolito.insertarDerecha(jtxt3.getText(), arbolito.getRaiz());
        //Nivel 2
        arbolito.insertarIzquierda(jtxt4.getText(), arbolito.getRaiz().getIzquierda());
        arbolito.insertarDerecha(jtxt5.getText(), arbolito.getRaiz().getIzquierda());
        arbolito.insertarIzquierda(jtxt6.getText(), arbolito.getRaiz().getDerecha());
        arbolito.insertarDerecha(jtxt7.getText(), arbolito.getRaiz().getDerecha());
        //Nivel 3
        arbolito.insertarIzquierda(jtxt8.getText(), arbolito.getRaiz().getIzquierda().getIzquierda());
        arbolito.insertarDerecha(jtxt9.getText(), arbolito.getRaiz().getIzquierda().getIzquierda());
    }

